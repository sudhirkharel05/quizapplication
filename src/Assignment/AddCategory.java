    //accessing the list of category from the category table of database
    public ArrayList<Category> subjectList(){
        ArrayList<Category>subjectList = new ArrayList<>();
        try{
        Connection con = MyConnection.getConnection();
        String query1 = "SELECT `category_id`, `subject` FROM `category`";
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(query1);
        Category category;
        while(rs.next()){
            category = new Category(rs.getInt("category_id"), rs.getString("subject"));
            subjectList.add(category); // adding the category 
            
        }
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        return subjectList;
    }
    public void show_category(){ // showing the category and displaying them in jtable
    ArrayList<Category> list = subjectList();
    DefaultTableModel model =(DefaultTableModel) jcategory.getModel();
    Object[] row = new Object[2];
    for(int i =0; i<list.size(); i++){
        //adding the data in row
        row[0]= list.get(i).getcategory_id();
        row[1]= list.get(i).getsubject();
        model.addRow(row);
        
    }
}

