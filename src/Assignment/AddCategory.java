    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
       if (txtcat.getText().equals(""))
        {
          JOptionPane.showMessageDialog(null,"Please give the Category name.");
        }
       else{
        String category = txtcat.getText();
       Connection con = MyConnection.getConnection();
       PreparedStatement ps;
      //adding the category in database
       try{
           ps = con.prepareStatement("INSERT INTO `category`(`subject`) VALUES (?)");
           ps.setString(1, category);
           ps.executeUpdate();
           //displaying the message as soon as the questioin is added.
           JOptionPane.showMessageDialog(null,"Subject has been added successfully");
           txtcat.setText("");
           
       }
       catch (SQLException ex) {
                Logger.getLogger(AddCategory.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
    }//GEN-LAST:event_btnaddActionPerformed

